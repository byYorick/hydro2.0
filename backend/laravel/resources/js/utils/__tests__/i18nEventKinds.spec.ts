describe('translateEventKind coverage', () => {
  it('переводит все известные zone event types на русский', () => {
    const missing: string[] = []
    for (const type of KNOWN_ZONE_EVENT_TYPES) {
      const translated = translateEventKind(type)
      if (!translated || translated === type) {
        missing.push(type)
      }
    }
    expect(missing, `Нет перевода для: ${missing.join(', ')}`).toEqual([])
  })

  it('не оставляет сырой UPPER_SNAKE в словаре как value', () => {
    for (const [key, value] of Object.entries(EVENT_KIND_TRANSLATIONS)) {
      expect(value, key).not.toBe(key)
      expect(value.length, key).toBeGreaterThan(0)
    }
  })
})
